//чтобы модифицировать строку, можно  использовать метод slice. Хотим отрезать слово world и как то использовать дальше

const logg = "Hello world"; // w Это 6 позиция, представить как 7 позиция но надо убрать ноль поэтому 6. Либо сразу с 0 считать
// 1 аргумент - с какой части строки нам надо возвращать что то, второй - где закончить отрезание (НЕ ВКЛЮЧАЯ), то есть если до 10 индекса, то 11, тк не включая 10 индекс
