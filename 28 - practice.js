console.log(logg.slice(-11, -5)); // Hello

// Ниже привел комбинированный пример, как найти слово и вырезать его
const someStr = "YO amogus PAPsAP";
const searchword = "amogus",
  indexStart = someStr.indexOf(searchword),
  wordIndexEnd = searchword.length - 1, //находим конец индекса путем вычитания единицы из длины (тк с нуля индексация),
  IndexEndInStr = indexStart + wordIndexEnd; //складываем тк нужен индекс именно в строке, а не в слове
indexStart !== -1 //проверка есть ли такое слово
  ? console.log(someStr.slice(indexStart, IndexEndInStr + 1)) //+1 тк невключительно, я для наглядности так оставил, не стал сразу там прибавлять
  : console.log("There is no such word or part of word");

/*
метод substring похож на slice, разница в том, что 1 аргумент можно задавать больше, 
чем второй (не стоит так делать) и не поддерживает отрицательные значения, если задать отрицательные значения, то будет интерпретироваться как 0.
*/
const logg = "Hello world";

console.log(logg.substring(6, 11));

console.log(logg.substr(0, 5)); // устаревший метод, второй аргумент - сколько символов надо вывести

